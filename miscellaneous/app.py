lines = multi_line_string.splitlines()
vocabulary = []

for line in lines:
    # 忽略空行
    if not line.strip():
        continue
    parts = line.split("\t")
    # 取前兩個欄位 (word 與 definition)
    if len(parts) >= 2:
        word = parts[0].strip()
        definition = parts[1].strip()
        vocabulary.append({'word': word, 'definition': definition})
    else:
        # 如無法正確分割，則略過
        continue

# 輸出 JS 內容
js_output = "const vocabulary = [\n"
for entry in vocabulary:
    js_output += f'  {{ word: "{entry["word"]}", definition: "{entry["definition"]}" }},\n'
js_output += "];\n"

# 將結果寫入檔案 (例如 vocabulary.js)
with open("vocabulary.js", "w", encoding="utf-8") as f:
    f.write(js_output)

